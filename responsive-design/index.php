      <h1>Développement adaptatif pour le web mobile</h1>

      <p>Il se vend aujourd'hui plus de téléphone portable que d'ordinateur, les smartphones sont ainsi devenu le premier moyen d'accéder à un site web avec leur lot de spécifités qu'il faut prendre en compte. Avoir un site adapté à ces supports est indispensable.</p>

      <p>Les <strong>CSS</strong> permet de gérer cela sans avoir à refaire complétement le site web. L'usage de <strong>media queries</strong> permet de simplement adapter la mise en forme d'un site en fonction des spécificités du support. </p>

      <p>Au dela des technologies, un site adapter pour mobile ne se conçoit pas de la même façon, l'internaute n'étant pas dans la même environement, il faut penser l'interface de la manière la plus efficace possible.</p>

      <p>Il est possible également de faire une adaptation également pour les tablettes et ainsi mettre en place trois interfaces adaptées.</p>

      <h2>Offre</h2>

      <p>Vous avez un site comportant 3 types de pages différentes (par exemple la page d'accueil, les page de contenus et un formulaire de contact), je vous transforme les feuilles de styles pour qu'elles s'adaptent au mobile, sans pour autant modifier le contenu.</p>

      <p>En fonction des éléments du site, du contenu et de la mise en page, je réalise les adaptations nécessaire à la consultation sur mobile directement sur le site pour pouvoir le tester directement.</p>

